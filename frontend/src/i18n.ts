export function getLanguage(): 'pt' | 'en' {
    return (localStorage.getItem('lang') as 'pt' | 'en') || 'pt';
}

export function setLanguage(lang: 'pt' | 'en') {
    localStorage.setItem('lang', lang);
    window.location.reload();
}

export function t(key: keyof typeof TRANSLATIONS.pt): string {
    const lang = getLanguage();
    return TRANSLATIONS[lang][key] || TRANSLATIONS.pt[key] || String(key);
}

export function setupLanguageToggle() {
    document.addEventListener('DOMContentLoaded', () => {
        const nav = document.querySelector('nav');
        if (!nav) return;
        
        // Check if selector already exists
        if (document.getElementById('lang-toggle-container')) return;
        
        const currentLang = getLanguage();
        const oppositeLang = currentLang === 'pt' ? 'en' : 'pt';
        const label = currentLang === 'pt' ? 'EN' : 'PT';
        
        const container = document.createElement('div');
        container.id = 'lang-toggle-container';
        container.style.display = 'flex';
        container.style.alignItems = 'center';
        container.style.marginLeft = '1rem';
        
        const btn = document.createElement('button');
        btn.textContent = label;
        btn.className = 'btn btn-ghost';
        btn.style.padding = '0.3rem 0.6rem';
        btn.style.fontSize = '0.85rem';
        btn.style.minWidth = 'unset';
        btn.style.borderColor = 'var(--gold)';
        btn.style.color = 'var(--gold)';
        
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            setLanguage(oppositeLang);
        });
        
        container.appendChild(btn);
        
        // Insert inside nav container (usually at the end of nav links)
        const ul = nav.querySelector('ul');
        if (ul) {
            const li = document.createElement('li');
            li.appendChild(container);
            ul.appendChild(li);
        } else {
            nav.appendChild(container);
        }
    });
}
